                <p class="paragrafo">Antes de nos aprofundarmos, é importante te contextualizar um pouco do cenário da educação para pessoas surdas no Brasil. Até o século passado, as instituições de ensino insistiam na oralização dos alunos surdos. Ou seja, eles não eram livres para aprender e se comunicar da forma que preferiam, e tinham que ser ensinados a falar português e fazer leitura labial.
                    <br>
