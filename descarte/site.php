                <h4 class="titulo">Qual a importância do ensino de Libras para alunos ouvintes?</h4>
                
                <p class="paragrafo">São vários os benefícios de uma pessoa ouvinte aprender Libras. O mais óbvio é que ela conseguirá melhorar sua comunicação com pessoas da comunidade surda no Brasil, mas as consequências dessa aproximação também são bem importantes. A pessoa ouvinte vai aprender sobre a cultura surda, valorizando cada vez mais a diversidade cultural e se aliando às pautas de inclusão. Ainda, vai ajudar a promover uma sociedade menos capacitista, independente da sua idade.
                    </p>
        
                <h5 class="titulo">Como é o ensino de Libras?</h5>
        
                <p class="paragrafo"> ensino de Libras muda de acordo com quem está aprendendo. Por exemplo, ensinar a Língua Brasileira de Sinais para uma pessoa surda será muito diferente do que ensinar o idioma para uma pessoa ouvinte. De qualquer forma, os educadores devem sempre ter em seu planejamento didático estratégias variadas que foquem na modalidade visual do ensino. 
        
                Mas vamos lá, o ensino de Libras para pessoas surdas é chamado de L1. Nele, o professor deve explorar principalmente o sentido visual, já que é assim que o aluno conseguirá aprender toda a estrutura gramatical da língua de sinais. Essa forma de ensino é conhecida como Pedagogia Visual ou Pedagogia da Diferença.
                    
                Já quando a Libras é ensinada para pessoas ouvintes, é chamada de L2. Com esse método o educador pode fazer uso tanto da visão para ensinar a maneira certa de se sinalizar, quanto a oralização para explicar o significado dos sinais. </p>           

