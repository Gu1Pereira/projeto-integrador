                </p>
                    <br>
                    <br>
                <p class="paragrafo">Por conta disso, hoje as escolas ainda não estão preparadas para acolher estudantes surdos. As pessoas surdas precisam de ambientes educacionais estimulantes, que desafiem o pensamento crítico e exercitem sua capacidade cognitiva. É então que a Libras entra em ação. Oferecer ensino de Libras é super importante para garantir um tratamento mais igualitário entre todos os alunos, para que eles tenham as mesmas oportunidades de aprendizagem, cada uma de acordo com suas necessidades. Além do que, educação em Libras é um direito por lei, mas calma que ainda vamos falar mais sobre isso.
                    <br>
                    <br>
                Por fim, o ensino de Libras é uma ótima maneira de valorizar e empoderar a comunidade surda e sua cultura, a deixando mais em evidência perante a sociedade como um todo.
